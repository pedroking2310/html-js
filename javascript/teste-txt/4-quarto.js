/**Programar a conversão de uma temperatura digitada pelo usuário em graus Celsius para Fahrenheit.
 *  Mostrar o resultado na tela. 
 */

let grausC, Fahrenheit
grausC = Number(prompt('digite o número de graus: '))

fahrenheit = (grausC * 1.8) + 32

alert(' a temperatura em fahrenheit é: ' + fahrenheit)
